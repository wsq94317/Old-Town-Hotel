// Personality traits that make staff memorable (Phase 4 Staff).
// Positive traits help; negative traits create the "cheap staff = trouble" tension.
public enum StaffTrait
{
    Charmer,    // + guest satisfaction
    FastHands,  // + work speed
    Tidy,       // + cleaning quality
    Calm,       // no slowdown under pressure
    Clumsy,     // occasional blunders (e.g. dirties a clean room)
    Ambitious,  // periodically demands a raise
    Lazy,       // drains stamina faster
    Diva,       // refuses to work when morale is low
    Cheap,      // low wage but lower stats
    Strict      // stricter / slower inspection
}
